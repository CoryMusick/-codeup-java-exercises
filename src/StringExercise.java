public class StringExercise {
    public static void main(String[] args) {
        String pinkFloyd = "We don't need no education\n" +
                "We dont need no thought control";
        System.out.println(pinkFloyd);
        System.out.println();
        String checkThisOut = "Check \"this\" out!, \"s inside of \"s";
        System.out.println(checkThisOut);
        System.out.println();
        String windows = "In windows, the main drive is usually C:\\";
        System.out.println(windows);
        System.out.println();
        String backslashes = "I can do backslashes \\, double back slashes \\\\, and the amazing triple backslash \\\\\\!";
        System.out.println(backslashes);
    }
}
